public class Caracteristica {
    private String clase;
    private String alimentacion;
    private String metodoNacimiento;
    private String habitat;

    public Caracteristica(String clase, String alimentacion, String metodoNacimiento, String habitat) {
        this.clase = clase;
        this.alimentacion = alimentacion;
        this.metodoNacimiento = metodoNacimiento;
        this.habitat = habitat;
    }
}
